package com.qf.test06;

/**
 * 相加
 */
public class Addition {

    /**
     * 两数整型相加
     * @param a 输入被加数
     * @param b 输入加数
     * @return  返回计算结果
     */
    public int Addition(int a,int b){
        return a + b;
    }

    /**
     * 两长整型相加
     * @param a 输入被加数
     * @param b 输入加数
     * @return  返回计算结果
     */
    public long Addition(long a,long b){
        return a + b;
    }

    /**
     * 两单精度相加
     * @param a 输入被加数
     * @param b 输入加数
     * @return  返回计算结果
     */
    public float Addition(float a,float b){
        return a + b;
    }

    /**
     * 两双精度相加
     * @param a 输入被加数
     * @param b 输入加数
     * @return  返回计算结果
     */
    public double Addition(double a,double b){
        return a + b;
    }

    /**
     * 两字符串相加
     * @param a 输入第一个字符串
     * @param b 输入第二个字符串
     * @return  返回结果
     */
    public String Addition(String a,String b){
        return a + b;
    }

}
