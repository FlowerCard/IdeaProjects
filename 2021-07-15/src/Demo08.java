import java.util.Scanner;

public class Demo08 {
    public static void main(String[] args) {
        //定义一个大 写字母，把它转换为小写字母后显示出来。
        String str = "A";
        System.out.println("定义的大写字母为：" + str);
        String lowerCase = str.toLowerCase();
        System.out.println("转为小写字母为：" + lowerCase);
    }
}
