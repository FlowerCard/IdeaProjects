
public class Demo03 {
    public static void main(String[] args){
        //定义基本数据类型，并输出变量的值
        int a = 10;
        System.out.println(a);
        float b = 10.0f;
        System.out.println(b);
        double c = 10.0;
        System.out.println(c);
        char d = 'a';
        System.out.println((int)d);
        boolean e = true;
        System.out.println(e);
    }
}
