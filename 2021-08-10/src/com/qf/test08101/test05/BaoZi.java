public class BaoZi {

    /**
     * 标记，判断有没有包子
     */
    private Boolean sign = false;

    /**
     * 皮
     */
    private String pi;

    /**
     * 馅
     */
    private String xian;

    public Boolean getSign() {
        return sign;
    }

    public void setSign(Boolean sign) {
        this.sign = sign;
    }

    public String getPi() {
        return pi;
    }

    public void setPi(String pi) {
        this.pi = pi;
    }

    public String getXian() {
        return xian;
    }

    public void setXian(String xian) {
        this.xian = xian;
    }
}
