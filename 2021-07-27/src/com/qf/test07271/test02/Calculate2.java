package com.qf.test07271.test02;

public class Calculate2 extends Calculate1{

    /**
     * 乘法
     * @param a 被乘数
     * @param b 乘数
     * @return  返回计算结果
     */
    public int multiplication(int a,int b){
        return a * b;
    }

    /**
     * 除法
     * @param a 被除数
     * @param b 除数
     * @return  返回计算结果
     */
    public int division(int a,int b){
        return a / b;
    }

}
