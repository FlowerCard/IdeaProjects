package com.qf.test07271.test02;

public class Calculate1 {

    /**
     * 加法
     * @param a 被加数
     * @param b 加数
     * @return  返回计算结果
     */
    protected int add(int a,int b){
        return a + b;
    }

    /**
     * 减法
     * @param a 被减数
     * @param b 减数
     * @return  返回计算结果
     */
    protected int subtraction(int a,int b){
        return a - b;
    }

}
